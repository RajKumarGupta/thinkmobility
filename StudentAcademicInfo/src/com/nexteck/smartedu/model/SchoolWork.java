package com.nexteck.smartedu.model;

public class SchoolWork {
	
	private Integer SchoolWorkType;
	private String Subject;
	private String Chapter;
	private String Description;
	private String WorkDate;
	private String WorkReviewDate;
	
	public Integer getSchoolWorkType() {
		return SchoolWorkType;
	}
	public void setSchoolWorkType(Integer schoolWorkType) {
		SchoolWorkType = schoolWorkType;
	}
	public String getSubject() {
		return Subject;
	}
	public void setSubject(String subject) {
		Subject = subject;
	}
	public String getChapter() {
		return Chapter;
	}
	public void setChapter(String chapter) {
		Chapter = chapter;
	}
	public String getDescription() {
		return Description;
	}
	public void setDescription(String description) {
		Description = description;
	}
	public String getWorkDate() {
		return WorkDate;
	}
	public void setWorkDate(String workDate) {
		WorkDate = workDate;
	}
	public String getWorkReviewDate() {
		return WorkReviewDate;
	}
	public void setWorkReviewDate(String workReviewDate) {
		WorkReviewDate = workReviewDate;
	}
	
	
}
